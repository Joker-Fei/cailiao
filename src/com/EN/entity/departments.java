package com.EN.entity;

public class departments {
	private String id;
	 
    private String dmName;
    
    private String dmIntroduce;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDmName() {
		return dmName;
	}

	public void setDmName(String dmName) {
		this.dmName = dmName;
	}

	public String getDmIntroduce() {
		return dmIntroduce;
	}

	public void setDmIntroduce(String dmIntroduce) {
		this.dmIntroduce = dmIntroduce;
	}

    
}
